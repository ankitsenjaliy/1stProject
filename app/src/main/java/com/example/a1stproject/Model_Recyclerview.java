package com.example.a1stproject;

public class Model_Recyclerview {

    int img;
    String name;

    public Model_Recyclerview(int img, String name) {
        this.img = img;
        this.name = name;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
